def strip_ruby(text):
    prev = None
    while prev != text:
        prev = text
        text = re.sub(r'<ruby>([^<]*?)<rt>[^<]*</rt></ruby>', r'\1', text)
    return text


def apply_ruby(text):
    if not re.search(r'[\u4e00-\u9fff]', text):
        return text, []
    text = strip_ruby(text)
    keys = sorted(RUBY.keys(), key=lambda k: -len(k))
    placeholders = {}
    for i, k in enumerate(keys):
        if k in text:
            ph = f'\x00K{i}\x00'
            text = text.replace(k, ph)
            placeholders[ph] = f'<ruby>{k}<rt>{RUBY[k]}</rt></ruby>'
    for ph, repl in placeholders.items():
        text = text.replace(ph, repl)
    remaining = re.findall(r'[\u4e00-\u9fff]+', re.sub(r'<ruby>.*?</ruby>', '', text))
    return text, remaining


def walk_and_apply(obj, missing):
    if isinstance(obj, dict):
        for k, v in list(obj.items()):
            if isinstance(v, str):
                if re.search(r'[\u4e00-\u9fff]', v):
                    new, rem = apply_ruby(v)
                    obj[k] = new
                    missing.update(rem)
            elif isinstance(v, (dict, list)):
                walk_and_apply(v, missing)
    elif isinstance(obj, list):
        for i, v in enumerate(obj):
            if isinstance(v, str):
                if re.search(r'[\u4e00-\u9fff]', v):
                    new, rem = apply_ruby(v)
                    obj[i] = new
                    missing.update(rem)
            elif isinstance(v, (dict, list)):
                walk_and_apply(v, missing)


def main():
    missing = set()
    for sec in ['goii', 'bunpo', 'chokkai']:
        path = f'test2_{sec}_questions.json'
        with open(path, encoding='utf-8') as f:
            data = json.load(f)
        walk_and_apply(data, missing)
        with open(path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f'  {path}: done')

    print(f'\n辞書漏れ候補: {len(missing)}')
    for r in sorted(missing):
        print(f'  {r}')


if __name__ == '__main__':
    main()
